"""Prueba de uso de números complejos en PyCharm"""

"""De esta manera es como establecemos los números complejos, que vienen formados por la parte real y la parte 
imaginaria dentro del paréntesis"""
x = 2 + 5j
y = 7j + 4
print(x)
print(y)
print('---------')

"""Con la siguiente sintáxis podemos pedir que solo nos muestren la parte real o la parte imaginaria del 
número complejo"""
print(x.real)
print(x.imag)
