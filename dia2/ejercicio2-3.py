z = 7

"""Los siguientes son los operadores básicos para la artimética"""
print(f'la division normal de {z} entre {y} es igual a {z/y}')
print(f'la division al piso de {z}, o el cociente, entre {y} es igual a {z//y}')
print(f'el módulo de {z}, o el residuo/resto, entre {y} es igual a {z%y}')
print(f'el valor {x} elevado a la {y} es {x**y}')
print(f'el valor {x} elevado a la {3} es {x**3}, lo cual también se logra con {pow(x, 3)}')

